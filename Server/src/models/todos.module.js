import mongoose, { Schema } from "mongoose";

const TodoSchema = new Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    age: {
      type: Number,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);
export const TodoModel = mongoose.model("todo", TodoSchema);
